//Logical Operator

// Logical And, untuk pengkondisian untuk 2 pembanding atau lebih
// dia bakal bernilai true jika keduanya bernilai true, selain itu semua false
// operatornya &&



//Logical Or
// jika salah satu pembanding bernilai true, maka hasilnya akan true
// jika kedua nilia bernilai false jika keduanya false
// operatornya ||


// Logical NOT (satu pembanding)
// akan bernilai true, jika pembandingnya false, vice-versa
// Operatornya !




// And
// Kedua pembanding harus bernilai true jika mau mendapat nilai true, selain itu false
console.log("And Operator");
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);
console.log("------------------\n");


// Or
// Kedua pembanding harus bernilai false jika mau mendapat nilai false, selain itu true
console.log("Or Operator");
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);
console.log("------------------\n");



// not
//kebalikan
console.log("Not Operator");
console.log(!true);
console.log(!false);
console.log("------------------\n");
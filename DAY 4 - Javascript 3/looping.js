// // for statement
for (i = 0; i < 10; i++) {
    console.log(i);
}
// while statement
let num = 6; // begin / inisialiasi awal
while (num < 6) { //ngecek konsisinya dulu baru menjalankan console
    console.log(`While : The Number is ${num}`);
    num++;
}
// console.log('----------');
// do while statement
do { //melakukan console dulu baru ngecek kondisi
    console.log(`Do While : The Number is ${num}`);
    num++;
} while (num < 6);